public class Televisor extends Electrodomestico{

    private float ancho;
    private float alto;
    private float fondo;

    public Televisor(double voltageUso) {
        super(voltageUso);
    }

    public void setAncho(float ancho) {
        this.ancho = ancho;
    }

    public void setAlto(float alto) {
        this.alto = alto;
    }

    public void setFondo(float fondo) {
        this.fondo = fondo;
    }
}





